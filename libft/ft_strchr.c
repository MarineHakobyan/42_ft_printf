#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i])
	{
		if ((char)c == s[i])
			return ((char *)(s + i));
	}
	if (c == 0 && s[i] == 0)
		return ((char *)(s + i));
	return (0);
}
